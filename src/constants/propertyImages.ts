// Función para obtener imágenes aleatorias basadas en el tipo de propiedad
export function getRandomPropertyImages(propertyType: string, count: number = 1): string[] {
  let imageArray: string[] = [];
  
  switch (propertyType.toLowerCase()) {
    case 'casa':
      imageArray = PROPERTY_IMAGES.luxuryHouses;
      break;
    case 'apartamento':
      imageArray = PROPERTY_IMAGES.modernApartments;
      break;
    case 'casa campestre':
      imageArray = PROPERTY_IMAGES.countryHouses;
      break;
    case 'penthouse':
      imageArray = PROPERTY_IMAGES.penthouses;
      break;
    case 'lote':
    case 'terreno':
      imageArray = PROPERTY_IMAGES.lots;
      break;
    case 'oficina':
      imageArray = PROPERTY_IMAGES.offices;
      break;
    case 'local':
    case 'comercial':
      imageArray = PROPERTY_IMAGES.commercial;
      break;
    case 'bodega':
      imageArray = PROPERTY_IMAGES.warehouses;
      break;
    default:
      // Si no coincide con ningún tipo específico, usar una mezcla
      imageArray = [
        ...PROPERTY_IMAGES.luxuryHouses,
        ...PROPERTY_IMAGES.modernApartments,
        ...PROPERTY_IMAGES.countryHouses,
        ...PROPERTY_IMAGES.penthouses
      ];
  }
  
  // Mezclar el array y tomar las primeras 'count' imágenes
  const shuffled = [...imageArray].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

// Función para obtener una imagen aleatoria de cualquier tipo
export function getRandomPropertyImage(): string {
  const allImages = [
    ...PROPERTY_IMAGES.luxuryHouses,
    ...PROPERTY_IMAGES.modernApartments,
    ...PROPERTY_IMAGES.countryHouses,
    ...PROPERTY_IMAGES.penthouses,
    ...PROPERTY_IMAGES.lots,
    ...PROPERTY_IMAGES.offices,
    ...PROPERTY_IMAGES.commercial,
    ...PROPERTY_IMAGES.warehouses
  ];
  
  return allImages[Math.floor(Math.random() * allImages.length)];
} 